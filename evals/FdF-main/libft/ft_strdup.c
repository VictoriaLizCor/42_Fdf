#include"libft.h"

char	*ft_strdup(const char *s)
{
	char	*dst;
	int		i;
	int		len;

	len = ft_strlen(s);
	dst = (char *)malloc(len + 1);
	i = 0;
	if (!dst)
		return (NULL);
	while (s[i] != '\0')
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
