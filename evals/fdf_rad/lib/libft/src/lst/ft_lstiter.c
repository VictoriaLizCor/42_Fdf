#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	if ((void *)f == NULL)
		return ;
	while (lst != (t_list *)NULL)
	{
		f(lst);
		lst = lst->next;
	}
}
